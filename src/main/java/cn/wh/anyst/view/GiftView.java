package cn.wh.anyst.view;

public class GiftView {
	String giftId;
	String giftName;
	String flowerQuantity;
	String openPrice;
	String intro;
	String giftPicUrl;

	public String getGiftId() {
		return giftId;
	}

	public void setGiftId(String giftId) {
		this.giftId = giftId;
	}

	public String getGiftName() {
		return giftName;
	}

	public void setGiftName(String giftName) {
		this.giftName = giftName;
	}

	public String getFlowerQuantity() {
		return flowerQuantity;
	}

	public void setFlowerQuantity(String flowerQuantity) {
		this.flowerQuantity = flowerQuantity;
	}

	public String getOpenPrice() {
		return openPrice;
	}

	public void setOpenPrice(String openPrice) {
		this.openPrice = openPrice;
	}

	public String getIntro() {
		return intro;
	}

	public void setIntro(String intro) {
		this.intro = intro;
	}

	public String getGiftPicUrl() {
		return giftPicUrl;
	}

	public void setGiftPicUrl(String giftPicUrl) {
		this.giftPicUrl = giftPicUrl;
	}
}
